SELECT COR.CHG_WHO,COR.CHG_DT,COR.ORDER_ID,CO.ORDER_STATUS,OW.*
FROM ARBOR.CUSTOMER_ORDER_REVISION COR, ARBOR.CUSTOMER_ORDER CO, ARBOR.ORDER_WORK_QUEUE OW
WHERE COR.CHG_WHO='AutoRenewal' AND (COR.CHG_DT)>='01-OCT-2024'
AND COR.ORDER_ID=CO.ORDER_ID AND CO.ORDER_STATUS<>6
AND COR.ORDER_ID=OW.ORDER_ID AND OW.STATUS<>50 
AND OW.TRANSACTION_ID<>99

